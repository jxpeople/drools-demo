package com.jxpeople.demo.model;


public class User {

    private String sex;  // 性别；男/女
    private Integer age; // 年龄

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
    
}
